#ifndef TEMPO_H
#define TEMPO_H


class tempo{

    private:

    	//////////////////////////////////////////////////////////////////////////////////
    	///     @brief Atributos de controle de tempo
    	//////////////////////////////////////////////////////////////////////////////////
        time_t static inicio;
        time_t static fim;


    public:

    	//////////////////////////////////////////////////////////////////////////////////
    	///     @brief Construtor
    	//////////////////////////////////////////////////////////////////////////////////
        tempo();

    	//////////////////////////////////////////////////////////////////////////////////
    	///     @brief Destrutor
    	//////////////////////////////////////////////////////////////////////////////////        
        virtual ~tempo();

    	//////////////////////////////////////////////////////////////////////////////////
    	///     @brief Inicia a contagem do tempo
    	//////////////////////////////////////////////////////////////////////////////////        
        void inciaContagem() throw (runtime_error);

    	//////////////////////////////////////////////////////////////////////////////////
    	///     @brief Pára a contagem do tempo
    	//////////////////////////////////////////////////////////////////////////////////
        void terminaContagem() throw (runtime_error);


    	//////////////////////////////////////////////////////////////////////////////////
    	///     @brief Retorna o tempo total gasto
    	//////////////////////////////////////////////////////////////////////////////////
		double tempoGasto() throw (runtime_error);

};

#endif // TEMPO_H
